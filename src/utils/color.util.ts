/**
 * Get the name of a color from its hex code
 *
 * @param {string} hex The hex code of the color
 * @returns {string} The name of the color
 */
export const getColorName = (hex: string): string => {
  const formattedHex = hex.slice(1, 7).toUpperCase();
  const color = names.find(color => color[0] === formattedHex);

  return color ? color[1] : `#${formattedHex}`;
};

/**
 * Check if a color is transparent
 *
 * @param hex The hex code of the color with alpha code
 */
export const isTransparent = (hex: string): boolean => {
  return hex.length === 9 && hex[0] === "#" && hex[7] !== "F" && hex[8] !== "F";
};
